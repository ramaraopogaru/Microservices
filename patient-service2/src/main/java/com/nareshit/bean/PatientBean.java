package com.nareshit.bean;

import java.io.Serializable;
import java.util.Date;


public class PatientBean implements Serializable {

	private String patientId;
	private String firstName;
	private String lastName;
	private String middleName;
	private String gender;
	private String bloodGroup;
	private String birthDate;
	private String phoneNumber;
	private String address;
	private String email;
	private String password;
	private String status;
	private String createdBy;
	private String createdDate;
	
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getPatientId() {
		return patientId;
	}
	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getMiddleName() {
		return middleName;
	}
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBloodGroup() {
		return bloodGroup;
	}
	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}
	public String getBirthDate() {
		return birthDate;
	}
	public void setBirthDate(String birthDate) {
		this.birthDate = birthDate;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	@Override
	public String toString() {
		return "PatientBean [patientId=" + patientId + ", firstName=" + firstName + ", lastName=" + lastName
				+ ", middleName=" + middleName + ", gender=" + gender + ", bloodGroup=" + bloodGroup + ", birthDate="
				+ birthDate + ", phoneNumber=" + phoneNumber + ", address=" + address + ", email=" + email
				+ ", password=" + password + ", status=" + status + ", createdBy=" + createdBy + ", createdDate="
				+ createdDate + "]";
	}
	
	
	
}
